package vc.com.cartorios.to;

public class CartorioTO {

	private Long id;

	private String descricao;

	public CartorioTO() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String desc) {
		this.descricao = desc;
	}
}
